const today = new Date();
const formatDate = (d: Date) => d.toISOString().split('T')[0];
const addDays = (d: Date, days: number) => {
  const n = new Date(d);
  n.setDate(n.getDate() + days);
  return n;
};
const addHours = (d: Date, hours: number) => {
  const n = new Date(d);
  n.setHours(n.getHours() + hours);
  return n;
};

